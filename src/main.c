#include "../include/minirt.h"

int createRGB(int r, int g, int b)
{
	return (255 << 24 | r << 16 | g << 8 | b);
}

int	main(void)
{
	printf("RED = %x\n", createRGB(0, 125, 0));
	return (0);
}
