#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;

	if (lst == 0)
		return (0);
	else
	{
		i = 0;
		while (lst != 0)
		{	
			lst = lst->next;
			i++;
		}
		return (i);
	}
}
